-- 트리거 생성: 판매 후 재고 업데이트
CREATE OR REPLACE TRIGGER basic.TRG_UPDATE_STOCK_AFTER_SALE
AFTER INSERT ON "BASIC"."SALES"
FOR EACH ROW
DECLARE
    v_product_id NUMBER := :NEW.PRODUCT_ID; -- 판매된 상품 ID
    v_sale_quantity NUMBER := :NEW.QUANTITY; -- 판매 수량
    v_required_quantity NUMBER; -- 필요 재고 수량
BEGIN
    -- 재고 업데이트 및 확인
    FOR ingredient_rec IN (
        SELECT pi."INGREDIENT_ID", pi."QUANTITY"
        FROM "BASIC"."PRODUCTINGREDIENTS" pi
        WHERE pi."PRODUCT_ID" = v_product_id
    ) LOOP
        -- 필요한 재고 수량 계산
        v_required_quantity := ingredient_rec."QUANTITY" * v_sale_quantity;

        -- 재고 업데이트
        UPDATE "BASIC"."INGREDIENTS"
        SET "STOCK" = "STOCK" - v_required_quantity
        WHERE "INGREDIENT_ID" = ingredient_rec."INGREDIENT_ID"
        AND "STOCK" >= v_required_quantity;

        -- 재고 부족 시 예외 처리
        IF SQL%ROWCOUNT = 0 THEN
            RAISE_APPLICATION_ERROR(-20001, 
                '재고 부족: INGREDIENT_ID = ' || ingredient_rec."INGREDIENT_ID");
        END IF;
    END LOOP;

    -- 판매된 재료 정보 기록
    FOR ingredient_rec2 IN (
        SELECT pi."INGREDIENT_ID", pi."QUANTITY"
        FROM "BASIC"."PRODUCTINGREDIENTS" pi
        WHERE pi."PRODUCT_ID" = v_product_id
    ) LOOP
        v_required_quantity := ingredient_rec2."QUANTITY" * v_sale_quantity;
        INSERT INTO "BASIC"."SALESINGREDIENTS" 
        (
            "SALE_ID", 
            "PRODUCT_ID", 
            "INGREDIENT_ID", 
            "QUANTITY"
        )
        VALUES
        (
            :NEW.SALE_ID, 
            v_product_id, 
            ingredient_rec2."INGREDIENT_ID", 
            v_required_quantity
        );
    END LOOP;
END;
